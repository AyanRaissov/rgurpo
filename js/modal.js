const openModal_01 = document.querySelector('.open_modal_01')
const clsModal_01 = document.querySelector('.cls_modal_01')
const modalWindow_01 = document.querySelector('.modal_window_01')

openModal_01.addEventListener('click', ()=>{
	modalWindow_01.style.display = 'flex'
	setTimeout(()=>{
		modalWindow_01.style.opacity = 1
	}, 300)
    window.onclick = function(event) {
        if (event.target == modalWindow_01) {
            modalWindow_01.style.display = "none";
        }
      }
})

clsModal_01.addEventListener('click', ()=>{
	modalWindow_01.style.opacity = 0
	setTimeout(()=>{
		modalWindow_01.style.display = 'none'
	}, 300)
})



